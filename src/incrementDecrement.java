public class incrementDecrement {
    public static void main(String[] args) {
        int a=8;
        int y = a++;
        int z = ++a;
        int w = a<<2;
        int u = a>>2;
        System.out.println(y);
        System.out.println(z);
        System.out.println(w);
        System.out.println(u);
    }
}
