package OOPS.Polymorphism.overloading;

public class overload
{
    public int add(int a, int b)
    {
        return a+b;
    }
    public int add(int a, int b, int c)
    {
        return a+b+c;
    }
    public int add(int a, int b, int c, int d)
    {
        return a+b+c+d;
    }
    public float add(float a, float b)
    {
        return a+b;
    }
}
